// Khai báo 3 biến với kiểu dữ liệu number
let number1: number;
let number2: number;
let result: number;

// Gán giá trị cho hai biến đầu tiên
number1 = 10;
number2 = 5;

// Thực hiện các phép tính cơ bản và gán kết quả cho biến thứ 3
result = number1 + number2;
console.log("Tổng:", result);

result = number1 - number2;
console.log("Hiệu:", result);

result = number1 * number2;
console.log("Tích:", result);

result = number1 / number2;
console.log("Thương:", result);